// Задача 3. Дано целое число N. Не используя функции стандартной библиотеки
// языка С, перевести число в строковое представление в десятичном виде.
char* task3(int N){
	static char a[12];
	int i = 10;
	if (N < 0) {
		N = -1 * N;
	}
	a[11] = '\0';
	if (N == 0) {
		a[10] = '0';
		return &a[10];
	}
	while (N > 0) {
		a[i--] = (N % 10) + '0';
		N /= 10;
	}
	if (N < 0) {
		a[i--] = '-';
	}
	return &a[i+1];
}
